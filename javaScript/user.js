function clickUser() {
  if (conteudo.style.display == 'block') {
    conteudo.style.display = 'none'
    fundocity.style.display = 'block'
    boxpreenche.style.display = 'none'
  }else {
    if (conteudo.style.display = 'block') {
      fundocity.style.display = 'none'
      boxpreenche.style.display = 'block'
    }
  }
}

